import mongoose from 'mongoose';

const StockTransactionSchema = new mongoose.Schema({
  stockId: {
    type: String,
    required: true,
  },
  quantity: {
    type: Number,
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
  productId: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now(),
    required: true,
  },
});

export default mongoose.model('stocktransaction', StockTransactionSchema);
